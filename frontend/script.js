document.addEventListener('DOMContentLoaded', () => {
    // Theme
    const savedTheme = localStorage.getItem('theme') || 'dark';
    setTheme(savedTheme);

    // Language
    setLang(currentLang);

    // Theme toggle
    document.getElementById('themeToggle').addEventListener('click', () => {
        const current = document.documentElement.getAttribute('data-theme');
        setTheme(current === 'dark' ? 'light' : 'dark');
    });

    // Language toggle
    document.getElementById('langToggle').addEventListener('click', () => {
        setLang(currentLang === 'pt' ? 'en' : 'pt');
    });

    // Mobile menu
    const mobileToggle = document.getElementById('mobileToggle');
    const navLinks = document.getElementById('navLinks');
    mobileToggle.addEventListener('click', () => {
        mobileToggle.classList.toggle('active');
        navLinks.classList.toggle('open');
    });
    navLinks.querySelectorAll('a').forEach(a => {
        a.addEventListener('click', () => {
            mobileToggle.classList.remove('active');
            navLinks.classList.remove('open');
        });
    });

    // Scroll effects
    const nav = document.getElementById('nav');
    const toTop = document.getElementById('toTop');
    const heroScroll = document.getElementById('heroScroll');

    window.addEventListener('scroll', () => {
        const y = window.scrollY;
        nav.classList.toggle('scrolled', y > 50);
        toTop.classList.toggle('visible', y > 400);
        if (heroScroll) {
            heroScroll.style.opacity = y > 80 ? '0' : '1';
            heroScroll.style.pointerEvents = y > 80 ? 'none' : 'auto';
        }
    });

    // Back to top
    toTop.addEventListener('click', () => window.scrollTo({ top: 0, behavior: 'smooth' }));

    // Scroll indicator click
    if (heroScroll) {
        heroScroll.addEventListener('click', () => {
            document.querySelector('#about').scrollIntoView({ behavior: 'smooth' });
        });
    }

    // Active nav on scroll
    const sections = document.querySelectorAll('section[id]');
    window.addEventListener('scroll', () => {
        let current = '';
        sections.forEach(s => {
            if (window.scrollY >= s.offsetTop - 200) current = s.id;
        });
        navLinks.querySelectorAll('a').forEach(a => {
            a.classList.toggle('active', a.getAttribute('href') === '#' + current);
        });
    });

    // Reveal on scroll
    const observer = new IntersectionObserver(entries => {
        entries.forEach(e => { if (e.isIntersecting) e.target.classList.add('visible'); });
    }, { threshold: 0.08, rootMargin: '0px 0px -40px 0px' });
    document.querySelectorAll('.reveal').forEach(el => observer.observe(el));

    // Skill tabs
    document.querySelectorAll('.skill-tab-btn').forEach(btn => {
        btn.addEventListener('click', () => {
            document.querySelectorAll('.skill-tab-btn').forEach(b => b.classList.remove('active'));
            document.querySelectorAll('.skills-panel').forEach(p => p.classList.remove('active'));
            btn.classList.add('active');
            document.getElementById('panel-' + btn.dataset.tab).classList.add('active');
        });
    });

    // Smooth scroll
    document.querySelectorAll('a[href^="#"]').forEach(a => {
        a.addEventListener('click', e => {
            e.preventDefault();
            const target = document.querySelector(a.getAttribute('href'));
            if (target) target.scrollIntoView({ behavior: 'smooth', block: 'start' });
        });
    });
});

//    SKILLS CAROUSEL — frontend/skills-carousel.js
(function() {

    var CARD_WIDTH = 140;
    var CARD_GAP = 16;
    var STEP = CARD_WIDTH + CARD_GAP;

    function initCarousel(topic) {
        var track = topic.querySelector('.carousel-track');
        var container = topic.querySelector('.carousel-track-container');
        var prevBtn = topic.querySelector('.carousel-btn.prev');
        var nextBtn = topic.querySelector('.carousel-btn.next');
        var dotsEl = topic.querySelector('.carousel-dots');

        if (!track || !container) return;

        var cards = Array.from(track.querySelectorAll('.skill-card'));
        var totalCards = cards.length;

        var currentIndex = 0;
        var visibleCount = 0;
        var maxIndex = 0;
        var isDragging = false;
        var startX = 0;
        var currentX = 0;
        var trackOffset = 0;
        var dots = [];

        function calcVisible() {
            var containerW = container.clientWidth;
            visibleCount = Math.max(1, Math.floor((containerW + CARD_GAP) / STEP));
            maxIndex = Math.max(0, totalCards - visibleCount);
        }

        function buildDots() {
            dotsEl.innerHTML = '';
            dots = [];
            var totalDots = maxIndex + 1;
            for (var i = 0; i < totalDots; i++) {
                (function(idx) {
                    var dot = document.createElement('button');
                    dot.className = 'carousel-dot' + (idx === 0 ? ' active' : '');
                    dot.setAttribute('aria-label', 'Slide ' + (idx + 1));
                    dot.addEventListener('click', function() { goTo(idx); });
                    dotsEl.appendChild(dot);
                    dots.push(dot);
                })(i);
            }
        }

        function goTo(index) {
            currentIndex = Math.max(0, Math.min(index, maxIndex));
            trackOffset = -(currentIndex * STEP);
            track.style.transform = 'translateX(' + trackOffset + 'px)';
            if (prevBtn) prevBtn.disabled = currentIndex === 0;
            if (nextBtn) nextBtn.disabled = currentIndex >= maxIndex;
            dots.forEach(function(d, i) {
                d.classList.toggle('active', i === currentIndex);
            });
        }

        if (prevBtn) {
            prevBtn.addEventListener('click', function() { goTo(currentIndex - 1); });
        }
        if (nextBtn) {
            nextBtn.addEventListener('click', function() { goTo(currentIndex + 1); });
        }

        /* Drag com mouse */
        track.addEventListener('mousedown', function(e) {
            isDragging = true;
            startX = e.clientX;
            currentX = trackOffset;
            track.classList.add('is-dragging');
        });

        window.addEventListener('mousemove', function(e) {
            if (!isDragging) return;
            var delta = e.clientX - startX;
            track.style.transform = 'translateX(' + (currentX + delta) + 'px)';
        });

        window.addEventListener('mouseup', function(e) {
            if (!isDragging) return;
            isDragging = false;
            track.classList.remove('is-dragging');
            var delta = e.clientX - startX;
            if (Math.abs(delta) > 50) {
                goTo(delta < 0 ? currentIndex + 1 : currentIndex - 1);
            } else {
                goTo(currentIndex);
            }
        });

        /* Touch */
        track.addEventListener('touchstart', function(e) {
            startX = e.touches[0].clientX;
            currentX = trackOffset;
        }, { passive: true });

        track.addEventListener('touchmove', function(e) {
            var delta = e.touches[0].clientX - startX;
            track.style.transform = 'translateX(' + (currentX + delta) + 'px)';
        }, { passive: true });

        track.addEventListener('touchend', function(e) {
            var delta = e.changedTouches[0].clientX - startX;
            if (Math.abs(delta) > 40) {
                goTo(delta < 0 ? currentIndex + 1 : currentIndex - 1);
            } else {
                goTo(currentIndex);
            }
        });

        /* Teclado */
        topic.setAttribute('tabindex', '0');
        topic.addEventListener('keydown', function(e) {
            if (e.key === 'ArrowLeft') {
                e.preventDefault();
                goTo(currentIndex - 1);
            }
            if (e.key === 'ArrowRight') {
                e.preventDefault();
                goTo(currentIndex + 1);
            }
        });

        /* Resize */
        var resizeTimer;
        window.addEventListener('resize', function() {
            clearTimeout(resizeTimer);
            resizeTimer = setTimeout(function() {
                calcVisible();
                buildDots();
                goTo(Math.min(currentIndex, maxIndex));
            }, 150);
        });

        calcVisible();
        buildDots();
        goTo(0);
    }

    function initAll() {
        document.querySelectorAll('.skills-topic').forEach(initCarousel);
    }

    if (document.readyState === 'loading') {
        document.addEventListener('DOMContentLoaded', initAll);
    } else {
        initAll();
    }

})();

(function() {

    function initHeroBg() {
        var hero = document.getElementById('home');
        if (!hero) return;

        var canvas = document.createElement('canvas');
        canvas.style.position = 'absolute';
        canvas.style.top = '0';
        canvas.style.left = '0';
        canvas.style.width = '100%';
        canvas.style.height = '100%';
        canvas.style.pointerEvents = 'none';
        canvas.style.zIndex = '0';

        hero.style.position = 'relative';
        hero.style.overflow = 'hidden';
        hero.insertBefore(canvas, hero.firstChild);

        var heroInner = hero.querySelector('.hero-inner');
        if (heroInner) {
            heroInner.style.position = 'relative';
            heroInner.style.zIndex = '1';
        }

        var heroScroll = hero.querySelector('.hero-scroll');
        if (heroScroll) { heroScroll.style.zIndex = '1'; }

        var ctx = canvas.getContext('2d');
        var W, H, raf;
        var tick = 0;

        // ── Paleta fiel ao portfolio ──
        // Dark:  --accent #818cf8  --bg-0 #09090b
        // Light: --accent #6366f1  --bg-0 #fafafa
        function getPalette() {
            var dark = document.documentElement.getAttribute('data-theme') !== 'light';
            if (dark) {
                return {
                    dark: true,
                    // Três tons da mesma família índigo — sem sair da paleta
                    c1: [129, 140, 248], // --accent dark      #818cf8
                    c2: [99, 102, 241], // um tom abaixo      #6366f1
                    c3: [165, 180, 252], // um tom acima       leve
                    bg: [9, 9, 11], // --bg-0 dark
                };
            } else {
                return {
                    dark: false,
                    c1: [99, 102, 241], // --accent light     #6366f1
                    c2: [129, 140, 248], // um tom acima       #818cf8
                    c3: [79, 70, 229], // um tom abaixo      #4f46e5
                    bg: [250, 250, 250], // --bg-0 light
                };
            }
        }

        // ── Orbs de neblina ──
        // Esferas de gradiente que se movem lentamente em trajetórias senoidais
        var orbs = [];

        function buildOrbs() {
            var p = getPalette();
            orbs = [
                // { cx, cy, r, color, speedX, speedY, phaseX, phaseY, rangeX, rangeY, opacity }
                { cx: 0.18, cy: 0.35, r: 0.52, col: p.c1, sx: 0.00021, sy: 0.00017, px: 0.0, py: 1.1, rx: 0.18, ry: 0.14, op: p.dark ? 0.13 : 0.10 },
                { cx: 0.78, cy: 0.55, r: 0.48, col: p.c2, sx: 0.00016, sy: 0.00023, px: 2.1, py: 0.4, rx: 0.16, ry: 0.18, op: p.dark ? 0.11 : 0.08 },
                { cx: 0.50, cy: 0.20, r: 0.40, col: p.c3, sx: 0.00019, sy: 0.00014, px: 1.0, py: 2.3, rx: 0.20, ry: 0.10, op: p.dark ? 0.09 : 0.07 },
                { cx: 0.85, cy: 0.20, r: 0.35, col: p.c1, sx: 0.00024, sy: 0.00018, px: 3.2, py: 0.9, rx: 0.12, ry: 0.16, op: p.dark ? 0.10 : 0.07 },
                { cx: 0.10, cy: 0.75, r: 0.38, col: p.c2, sx: 0.00014, sy: 0.00021, px: 0.7, py: 1.8, rx: 0.14, ry: 0.12, op: p.dark ? 0.08 : 0.06 },
                { cx: 0.60, cy: 0.80, r: 0.44, col: p.c3, sx: 0.00018, sy: 0.00013, px: 1.5, py: 3.0, rx: 0.16, ry: 0.14, op: p.dark ? 0.09 : 0.06 },
            ];
        }

        // ── Partículas finas flutuantes ──
        var particles = [];
        var PART_COUNT = 55;

        function buildParticles() {
            var p = getPalette();
            particles = [];
            var cols = [p.c1, p.c2, p.c3];
            for (var i = 0; i < PART_COUNT; i++) {
                var col = cols[Math.floor(Math.random() * cols.length)];
                var depth = 0.25 + Math.random() * 0.75;
                particles.push({
                    x: Math.random() * W,
                    y: Math.random() * H,
                    vx: (Math.random() - 0.5) * 0.22 * depth,
                    vy: -0.08 - Math.random() * 0.18 * depth, // sobem suavemente
                    r: (0.8 + Math.random() * 1.4) * depth,
                    col: col,
                    depth: depth,
                    alpha: (0.2 + Math.random() * 0.45) * depth,
                    phase: Math.random() * Math.PI * 2,
                    freq: 0.006 + Math.random() * 0.010,
                    drift: (Math.random() - 0.5) * 0.009, // oscilação lateral
                });
            }
        }

        function resize() {
            W = canvas.width = canvas.offsetWidth;
            H = canvas.height = canvas.offsetHeight;
            buildOrbs();
            buildParticles();
        }

        function draw() {
            tick++;
            ctx.clearRect(0, 0, W, H);

            var p = getPalette();

            // ── 1. Orbs de neblina ──
            for (var i = 0; i < orbs.length; i++) {
                var o = orbs[i];
                var cx = (o.cx + Math.sin(tick * o.sx + o.px) * o.rx) * W;
                var cy = (o.cy + Math.cos(tick * o.sy + o.py) * o.ry) * H;
                var r = o.r * Math.min(W, H);

                var grd = ctx.createRadialGradient(cx, cy, 0, cx, cy, r);
                grd.addColorStop(0, 'rgba(' + o.col[0] + ',' + o.col[1] + ',' + o.col[2] + ',' + (o.op * 1.6) + ')');
                grd.addColorStop(0.35, 'rgba(' + o.col[0] + ',' + o.col[1] + ',' + o.col[2] + ',' + o.op + ')');
                grd.addColorStop(0.7, 'rgba(' + o.col[0] + ',' + o.col[1] + ',' + o.col[2] + ',' + (o.op * 0.3) + ')');
                grd.addColorStop(1, 'rgba(' + o.col[0] + ',' + o.col[1] + ',' + o.col[2] + ',0)');

                ctx.fillStyle = grd;
                ctx.fillRect(0, 0, W, H);
            }

            // ── 2. Linha de horizonte luminosa ──
            // Uma faixa horizontal suave que pulsa — ancora o olhar
            var lineY = H * (0.52 + Math.sin(tick * 0.0009) * 0.04);
            var lineH = H * 0.28;
            var lineGrd = ctx.createLinearGradient(0, lineY - lineH, 0, lineY + lineH);
            var lc = p.c1;
            var lop = p.dark ? 0.055 : 0.04;
            lineGrd.addColorStop(0, 'rgba(' + lc[0] + ',' + lc[1] + ',' + lc[2] + ',0)');
            lineGrd.addColorStop(0.4, 'rgba(' + lc[0] + ',' + lc[1] + ',' + lc[2] + ',' + (lop * 0.5) + ')');
            lineGrd.addColorStop(0.5, 'rgba(' + lc[0] + ',' + lc[1] + ',' + lc[2] + ',' + lop + ')');
            lineGrd.addColorStop(0.6, 'rgba(' + lc[0] + ',' + lc[1] + ',' + lc[2] + ',' + (lop * 0.5) + ')');
            lineGrd.addColorStop(1, 'rgba(' + lc[0] + ',' + lc[1] + ',' + lc[2] + ',0)');
            ctx.fillStyle = lineGrd;
            ctx.fillRect(0, lineY - lineH, W, lineH * 2);

            // ── 3. Partículas finas ──
            for (var i = 0; i < particles.length; i++) {
                var pt = particles[i];

                pt.phase += pt.freq;
                pt.x += pt.vx + Math.sin(pt.phase) * pt.drift * W;
                pt.y += pt.vy;

                // Wrap: quando sobe para fora, reaparece em baixo
                if (pt.y < -10) {
                    pt.y = H + 10;
                    pt.x = Math.random() * W;
                }
                if (pt.x < -10) pt.x = W + 10;
                if (pt.x > W + 10) pt.x = -10;

                // Piscar suave
                var flicker = 0.75 + Math.sin(pt.phase * 2.3) * 0.25;
                var alpha = pt.alpha * flicker;

                // Ponto com halo mínimo
                var halo = ctx.createRadialGradient(pt.x, pt.y, 0, pt.x, pt.y, pt.r * 3.5);
                halo.addColorStop(0, 'rgba(' + pt.col[0] + ',' + pt.col[1] + ',' + pt.col[2] + ',' + (alpha * 0.7) + ')');
                halo.addColorStop(1, 'rgba(' + pt.col[0] + ',' + pt.col[1] + ',' + pt.col[2] + ',0)');
                ctx.beginPath();
                ctx.fillStyle = halo;
                ctx.arc(pt.x, pt.y, pt.r * 3.5, 0, Math.PI * 2);
                ctx.fill();

                ctx.beginPath();
                ctx.fillStyle = 'rgba(' + pt.col[0] + ',' + pt.col[1] + ',' + pt.col[2] + ',' + alpha + ')';
                ctx.arc(pt.x, pt.y, pt.r, 0, Math.PI * 2);
                ctx.fill();
            }

            // ── 4. Vinheta suave nas bordas — concentra o olhar no centro ──
            var vig = ctx.createRadialGradient(W * 0.5, H * 0.5, H * 0.25, W * 0.5, H * 0.5, H * 0.85);
            if (p.dark) {
                vig.addColorStop(0, 'rgba(9,9,11,0)');
                vig.addColorStop(1, 'rgba(9,9,11,0.45)');
            } else {
                vig.addColorStop(0, 'rgba(250,250,250,0)');
                vig.addColorStop(1, 'rgba(250,250,250,0.4)');
            }
            ctx.fillStyle = vig;
            ctx.fillRect(0, 0, W, H);

            raf = requestAnimationFrame(draw);
        }

        var resizeTimer;
        window.addEventListener('resize', function() {
            clearTimeout(resizeTimer);
            resizeTimer = setTimeout(function() {
                cancelAnimationFrame(raf);
                resize();
                draw();
            }, 200);
        });

        var btn = document.getElementById('themeToggle');
        if (btn) {
            btn.addEventListener('click', function() {
                setTimeout(function() {
                    buildOrbs();
                    buildParticles();
                    ctx.clearRect(0, 0, W, H);
                }, 80);
            });
        }

        resize();
        draw();
    }

    if (document.readyState === 'loading') {
        document.addEventListener('DOMContentLoaded', initHeroBg);
    } else {
        initHeroBg();
    }

})();